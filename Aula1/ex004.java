package Aula1;

public class ex004{

    public static void main(String[]arg) {
        char x; // é aceito apenas um caracter
        int y; 
        x= 'u'; // para usar a class "char" é necessario o uso de aspas simples dentro da variavel ex (x= 'u')
        y= 12;
        System.out.println("teste 4 " + x);
        System.out.println("teste " + y);
    }

}

//Nesse exemplo, o programa declara uma variável do tipo char para armazenar um único caractere e uma variável do tipo int para armazenar um número inteiro. Em seguida, atribui valores a essas variáveis e exibe os resultados na tela.
//Lembre-se de que o tipo char é usado para representar caracteres individuais, como letras, números ou símbolos, e deve ser atribuído usando aspas simples (' ').
//O tipo int é usado para representar números inteiros, que não possuem parte decimal.
//Esse código é um exemplo simples de declaração e uso de variáveis em Java.
//Diferença entre char e String 
//O char é um tipo de dado que representa um único caractere, como uma letra, número ou símbolo. Ele é declarado usando aspas simples (' '), por exemplo: char letra = 'A';.
//A String, por outro lado, é uma sequência de caracteres, ou seja, uma cadeia de texto. Ela é declarada usando aspas duplas (" "), por exemplo: String nome = "João";.
//Em resumo, use char quando precisar armazenar um único caractere e String quando precisar armazenar uma sequência de caracteres ou texto.