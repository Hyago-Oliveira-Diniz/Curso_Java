//Diferença entre boolean e int
//O boolean é um tipo de dado que representa valores lógicos, ou seja, verdadeiro (true) ou falso (false). Ele é usado para expressar condições e tomar decisões em estruturas condicionais, como if e while.
//O int, por outro lado, é um tipo de dado que representa números inteiros, ou seja, números sem parte decimal. Ele é usado para armazenar valores numéricos, como contagens, índices de arrays, etc.
//Em resumo, use boolean para valores lógicos (true/false) e int para números inteiros. 
