package Aula0;

public class ex001{ //estrutura base de um programa java 
    public static void main( String [] args){ // no lugar de string pode ser usado 
        System.out.print("Hello,Word"); // pode se usar print ou println quando quiser pular uma linha 
    } 
}
//Nesse exemplo, o programa imprime "Hello, Word" na tela. A estrutura básica de um programa Java inclui a declaração da classe (ex001) e o método principal (main), que é o ponto de entrada do programa. O comando System.out.print é usado para exibir texto na saída padrão (geralmente a tela do console).
//Diferença entre print e println
//O print exibe o texto ou valor na mesma linha, enquanto o println exibe o
//texto ou valor e move o cursor para a próxima linha, permitindo que a próxima saída comece em uma nova linha.
//Use print quando quiser continuar na mesma linha e println quando quiser iniciar uma nova linha após a
//saída.
//Exemplo:
//System.out.print("Olá, ");
//System.out.print("mundo!");
//Saída: Olá, mundo!
//System.out.println("Olá, ");
//System.out.println("mundo!");
//Saída:
//Olá,
//mundo!